
package Model;

public class Venda {
    private int VEN_IN_ID;
    private int PES_IN_ID;
    private String VEN_DT_VENDA;
    private float VEN_RE_TOTAL;
    private String VEN_ST_STATUS;

    public Venda() {
    }

    public int getVEN_IN_ID() {
        return VEN_IN_ID;
    }

    public void setVEN_IN_ID(int VEN_IN_ID) {
        this.VEN_IN_ID = VEN_IN_ID;
    }

    public int getPES_IN_ID() {
        return PES_IN_ID;
    }

    public void setPES_IN_ID(int PES_IN_ID) {
        this.PES_IN_ID = PES_IN_ID;
    }

    public String getVEN_DT_VENDA() {
        return VEN_DT_VENDA;
    }

    public void setVEN_DT_VENDA(String VEN_DT_VENDA) {
        this.VEN_DT_VENDA = VEN_DT_VENDA;
    }

    public float getVEN_RE_TOTAL() {
        return VEN_RE_TOTAL;
    }

    public void setVEN_RE_TOTAL(float VEN_RE_TOTAL) {
        this.VEN_RE_TOTAL = VEN_RE_TOTAL;
    }

    public String getVEN_ST_STATUS() {
        return VEN_ST_STATUS;
    }

    public void setVEN_ST_STATUS(String VEN_ST_STATUS) {
        this.VEN_ST_STATUS = VEN_ST_STATUS;
    }
    
    
}
