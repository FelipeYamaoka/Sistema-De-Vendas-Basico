
package Model;

public class Saida {
    private int SAI_IN_ID;
    private int PES_IN_ID;
    private String SAI_DT_DATASAIDA;
    private float SAO_RE_VALOR;
    private String SAI_ST_DESCRICAO;

    public Saida() {
    }

    public int getSAI_IN_ID() {
        return SAI_IN_ID;
    }

    public void setSAI_IN_ID(int SAI_IN_ID) {
        this.SAI_IN_ID = SAI_IN_ID;
    }

    public int getPES_IN_ID() {
        return PES_IN_ID;
    }

    public void setPES_IN_ID(int PES_IN_ID) {
        this.PES_IN_ID = PES_IN_ID;
    }

    public String getSAI_DT_DATASAIDA() {
        return SAI_DT_DATASAIDA;
    }

    public void setSAI_DT_DATASAIDA(String SAI_DT_DATASAIDA) {
        this.SAI_DT_DATASAIDA = SAI_DT_DATASAIDA;
    }

    public float getSAO_RE_VALOR() {
        return SAO_RE_VALOR;
    }

    public void setSAO_RE_VALOR(float SAO_RE_VALOR) {
        this.SAO_RE_VALOR = SAO_RE_VALOR;
    }

    public String getSAI_ST_DESCRICAO() {
        return SAI_ST_DESCRICAO;
    }

    public void setSAI_ST_DESCRICAO(String SAI_ST_DESCRICAO) {
        this.SAI_ST_DESCRICAO = SAI_ST_DESCRICAO;
    }
    
    
}
